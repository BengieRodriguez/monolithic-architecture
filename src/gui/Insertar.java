    private void btn5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn5ActionPerformed
        // TODO add your handling code here:
        String matricula_veh = txt8.getText();
        String marca_veh = txt9.getText();
        String color_veh = txt10.getText();
        String modelo_veh = txt11.getText();
        String capacidad_veh = txt12.getText();
        int par_capacidad_veh = Integer.parseInt(capacidad_veh);
        String condicion_veh = txt13.getText();
        String prestador_veh = txt14.getText();
        int par_prestador_veh = Integer.parseInt(prestador_veh);
        Vehiculo vehiculo = new Vehiculo(matricula_veh, marca_veh, color_veh, modelo_veh, par_capacidad_veh, condicion_veh, par_prestador_veh);
        per.guardarVehiculo(vehiculo);
        JOptionPane.showMessageDialog(this, "Vehiculo guardaado con exito");
    }//GEN-LAST:event_btn5ActionPerformed

