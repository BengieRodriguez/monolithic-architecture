    private void btn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn2ActionPerformed
        // TODO add your handling code here:
        String nombre_pre = txt4.getText();
        String telefono_pre = txt5.getText();
        String correo_pre = txt6.getText();
        String saldo_pre = txt7.getText();
        if (saldo_pre.isEmpty()) {
            saldo_pre = "0";
        }
        float new_saldo = Float.parseFloat(saldo_pre);
        Prestador presta = new Prestador(nombre_pre, correo_pre, telefono_pre, tipo, new_saldo);
        per.guardarPrestador(presta);
        JOptionPane.showMessageDialog(this, "Prestador guardaado con exito");
    }//GEN-LAST:event_btn2ActionPerformed

